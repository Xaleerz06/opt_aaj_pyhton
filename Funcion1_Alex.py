# Crea una función que reciba dos parámetros. El primero será el 
# saludo y el segundo será la persona a la que va dirigido el saludo. 
# Deberá imprimir por pantalla el saludo correspondiente. Invoca la función para comprobar su funcionamiento.

def saludar(saludo, nombre):
    print(saludo + ", " + nombre + "!")

saludar("Hola", "María")
saludar("Buenos días", "Carlos")
