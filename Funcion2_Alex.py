# Crea una función que reciba una variable y nos diga cuál es su tipo.


def mostrar(variable):
    print("El tipo de la variable es:", type(variable))


mostrar(42)            # Entero
mostrar(3.14)          # Flotante
mostrar("Hola")        # Cadena de texto
mostrar([1, 2, 3])     # Lista
mostrar({"a": 1})      # Diccionario
